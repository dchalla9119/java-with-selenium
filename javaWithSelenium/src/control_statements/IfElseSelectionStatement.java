package control_statements;

public class IfElseSelectionStatement {
	public static void main(String[] args) {
		int n1 = 2, n2 = 3;
		if(n1==n2)
		{
			System.out.println("hello");
		}
		else
		{
			System.out.println("hi");
		}
		System.out.println("normal statement");
	}

}

