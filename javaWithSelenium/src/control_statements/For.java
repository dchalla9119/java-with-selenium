package control_statements;

public class For {
	public static void main(String[] args) {
		for(int i = 1; i<=10; i++)
		{
			System.out.println(i+"hello");
		}//end of loop
		System.out.println("out of loop");
	}//end of main()
	}//end of class

////output:
//1hello
//2hello
//3hello
//4hello
//5hello
//6hello
//7hello
//8hello
//9hello
//10hello
//out of loop
