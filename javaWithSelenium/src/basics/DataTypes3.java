package basics;

public class DataTypes3 {

	public static void main(String[] args) {
		final int n1 = 1;
		final float n2 = 0.205f;
		final double n3 = 25;
		final char n4 = 's';
		final String n5 = "gsd";

	System.out.println(n1);
	System.out.println(n2);
	System.out.println(n3);
	System.out.println(n4);
	System.out.println(n5);
}

}


////output:
//1
//0.205
//25.0
//s
//gsd
